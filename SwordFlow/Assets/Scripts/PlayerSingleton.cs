using UnityEngine;

public class PlayerSingleton : MonoBehaviour
{

    private static PlayerSingleton _instance;

    void Awake()
    {
        if (_instance == null)
        {
            _instance = this;
            DontDestroyOnLoad(this.gameObject);
        }
        else
        {
            Destroy(this.gameObject);
        }
    }
}
